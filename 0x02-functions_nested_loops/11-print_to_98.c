#include "main.h"
/**
* print_to_98 - Entry point
* @n: the value
* Return: void
*/

void print_to_98(int n)
{
	while (n < 98 && n != 98)
	{
		if (n = 98)
		{
			_putchar(n + '0');
		}
		_putchar(n + '0');
		_putchar(44);
		_putchar(32);
		n++;
	}
	while (n > 98 && n != 98)
	{
		if (n = 98)
		{
			_putchar(n + '0');
		}
		_putchar(n + '0');
		_putchar(44);
		_putchar(32);
		n--;
	}
}
